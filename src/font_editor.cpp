void print_char (unsigned char *c)
{
	for (int i=0; i<8; ++i) {
		fprintf(stdout, "\"");
	  for (int k=0;k<6;++k){
		if (c[k] & (0x80 >> i))
			putchar('*');
		else
			putchar(' ');

	  }
	  fprintf(stdout, "\",");
	
	putchar('\n');
	}
}

void read_file(char *filename)
{
	char str[256*6];
	FILE *fp;
	fp = fopen(filename, "rb"); 
	if (!fp) {
		fprintf(stderr, "Unable to open file %s", filename);
		return;
	}
	unsigned char c;
	unsigned i = 0;
	do {
		c = fgetc(fp);
		str[i] = c; ++i;
	}while(c != '\0');
	i=0;
	do {
		c = fgetc(fp);
		str[i] = c; ++i;
	}while(c != '\0');

	//fread(str, 160*6, 1, fp); // skip 160 chars

	for(i=0;i<256;++i){
		fread(str, 1, 6, fp);
		print_char((unsigned char*)str);
	}

	fclose(fp);  /* close the input file */
}

unsigned char get_bit_value(unsigned char c, unsigned int pos)
{
	if (c == '*')
		return 1 << pos;
	return 0;
}

